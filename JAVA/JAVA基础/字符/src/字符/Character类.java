package 字符;

public class Character类 {
	public static void main(String[] args){
		System.out.println(Character.isLetter('a'));	//判断a 是否是字母
		System.out.println(Character.isLetter('1'));	//判断1 是否是字母
		System.out.println(Character.isDigit('a'));	//判断1 是否是数字
		System.out.println(Character.isDigit('1'));	//判断1 是否是
	}
}
